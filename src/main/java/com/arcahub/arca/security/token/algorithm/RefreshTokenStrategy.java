package com.arcahub.arca.security.token.algorithm;

import com.arcahub.arca.security.enums.TokenType;
import com.arcahub.arca.security.secret.Secrets;
import io.jsonwebtoken.JwtException;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;
import org.springframework.stereotype.Component;

import java.util.Date;

@Component
public class RefreshTokenStrategy implements TokenStrategy {
    private final String secret = Secrets.JWT_SECRET;
    private final long expiration = Secrets.REFRESH_JWT_EXPIRE_IN;

    @Override
    public TokenType getTokenType() {
        return TokenType.REFRESH;
    }

    @Override
    public String generateToken(String username) {
        return Jwts.builder()
                .setSubject(username)
                .setIssuedAt(new Date())
                .setExpiration(new Date(System.currentTimeMillis() + expiration))
                .signWith(SignatureAlgorithm.HS256, secret)
                .compact();
    }

    @Override
    public String getUsernameFromToken(String token) {
        return Jwts.parser()
                .setSigningKey(secret)
                .parseClaimsJws(token)
                .getBody()
                .getSubject();
    }

    @Override
    public boolean validateToken(String token) {
        try {
            Jwts.parser().setSigningKey(secret).parseClaimsJws(token);
            return true;
        } catch (JwtException | IllegalArgumentException e) {
            return false;
        }
    }
}
